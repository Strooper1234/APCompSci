
public class Period {
	
	int period;
	String subject;
	char grade;
	
	public Period(int period, String subject, char grade){
		this.period = period;
		this.subject = subject;
		this.grade = grade;
	}

	public int getPeriod() {
		return period;
	}

	public String getSubject() {
		return subject;
	}

	public char getGrade() {
		return grade;
	}
	
	
	
	
}
